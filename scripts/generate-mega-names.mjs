// Build-time generator for src/mega-names.generated.js.
//
// snInstanceModules re-exports each platform package (@servicenow/ui-core, the
// snabbdom renderer) from the shared instance bundle by emitting a virtual module
// with static named exports (`export const X = __ns["X"]`). Those names must be
// known when the virtual module source is generated, but the runtime namespace
// only exists in the browser, so we instead lex the export surface of the npm
// packages the addon is built against and freeze the result into a committed
// data file.
//
// Why the npm package and not the instance: a consumer's component source can
// only import names that exist in the @servicenow/* version it's authored
// against, so the npm package's export surface covers everything we need to
// re-export.
//
// Run via `npm run gen:mega-names`; also runs on `prepack`. Re-run when bumping
// the @servicenow/* devDependencies.

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { createRequire } from 'node:module'

const require = createRequire(import.meta.url)
const here = path.dirname(fileURLToPath(import.meta.url))
const addonRoot = path.resolve(here, '..')

// Platform packages whose named bindings components import. Components
// (now-button, etc.) are NOT here â€” they load their own module as side effects.
const PACKAGES = ['@servicenow/ui-core', '@servicenow/ui-renderer-snabbdom']

// es-module-lexer isn't a direct dependency â€” it ships with Vite (always present
// wherever this addon is developed/built), so resolve it through Vite.
function loadLexer() {
  try {
    const viteRequire = createRequire(require.resolve('vite/package.json'))
    return viteRequire('es-module-lexer')
  } catch (err) {
    throw new Error(
      'Could not load es-module-lexer via Vite. Install dependencies first ' +
        `(vite is a peer/devDependency of the addon). Original error: ${err.message}`,
    )
  }
}

// Resolve a package's ESM entry (prefer `module`, fall back to `main`) from the
// addon's own resolution scope.
function entryFor(pkg) {
  const pkgRequire = createRequire(path.join(addonRoot, 'package.json'))
  const pkgJsonPath = pkgRequire.resolve(`${pkg}/package.json`)
  const pkgJson = JSON.parse(fs.readFileSync(pkgJsonPath, 'utf8'))
  const entryRel = pkgJson.module ?? pkgJson.main
  if (!entryRel) {
    throw new Error(`${pkg} has no "module" or "main" entry to lex`)
  }
  return { entry: path.join(path.dirname(pkgJsonPath), entryRel), version: pkgJson.version }
}

async function main() {
  const lexer = loadLexer()
  await lexer.init

  const result = {}
  for (const pkg of PACKAGES) {
    const { entry, version } = entryFor(pkg)
    const [, exports] = lexer.parse(fs.readFileSync(entry, 'utf8'))
    const all = exports.map((e) => e.n)
    const names = all.filter((n) => n !== 'default')
    if (!names.length) {
      // A re-export facade (`export * from â€¦`) yields no names the lexer can see.
      throw new Error(
        `${pkg} entry (${entry}) exposed no named exports â€” its build may be a ` +
          're-export facade the lexer cannot follow. Update the generator.',
      )
    }
    result[pkg] = { version, hasDefault: all.includes('default'), names }
    console.log(`  ${pkg}@${version}: ${names.length} names, hasDefault=${all.includes('default')}`)
  }

  const body =
    '// AUTO-GENERATED by scripts/generate-mega-names.mjs â€” do not edit by hand.\n' +
    '// Re-export surface of the @servicenow/* npm packages the addon was built\n' +
    '// against, frozen at build time. See the generator for why these come from\n' +
    "// the npm package rather than the instance. Regenerate with `npm run gen:mega-names`.\n\n" +
    'export const PLATFORM_EXPORTS = ' +
    JSON.stringify(result, null, 2) +
    '\n'

  const outPath = path.join(addonRoot, 'src', 'mega-names.generated.js')
  fs.writeFileSync(outPath, body)
  console.log(`Wrote ${path.relative(addonRoot, outPath)}`)
}

main().catch((err) => {
  console.error(`[gen:mega-names] ${err.message}`)
  process.exit(1)
})
